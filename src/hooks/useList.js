import useTable from 'src/hooks/useTable';
import PropTypes from 'prop-types';

useList.propTypes = {
  defaultRowsPerPage: PropTypes.number,
};
export default function useList({ defaultRowsPerPage }) {
  const {
    dense,
    page,
    order,
    orderBy,
    rowsPerPage,
    setPage,
    //
    selected,
    setSelected,
    onSelectRow,
    onSelectAllRows,
    //
    onSort,
    onChangeDense,
    onChangePage,
    onChangeRowsPerPage,
  } = useTable();

  return {
    dense,
    page,
    order,
    orderBy,
    rowsPerPage: defaultRowsPerPage || rowsPerPage,
    setPage,
    //
    selected,
    setSelected,
    onSelectRow,
    onSelectAllRows,
    //
    onSort,
    onChangeDense,
    onChangePage,
    onChangeRowsPerPage,
  };
}
